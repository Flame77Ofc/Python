# Comentários
# Os comentários são trechos de texto no código que são ignorados pelo computador. Eles são usados para explicar o que o código faz, facilitar a leitura ou desativar temporariamente partes do código.

# Indentação
# A indentação é o espaçamentos utilizado para indicar a hierarquia entre blocos de código. Ela define se uma parte está ou não contida dentro de outra, como em loops ou funções (assuntos mais avançados). A indentação é obrigatória.