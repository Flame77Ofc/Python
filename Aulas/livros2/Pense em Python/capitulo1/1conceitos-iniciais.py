# Execução do python
# Para executar o python em seu computador, instale o site oficial (https://www.python.org/downloads/) e instale um editor de códigos, como o VScode.


# O primeiro programa
# Tradicionalmente, o primeiro programa que se escreve em uma nova linguagem chama-se “Hello, World!”, porque tudo o que faz é exibir as palavras “Hello, World!” na tela. No Python, ele se parece com isto:
print('Hello, World!')
# Isso será executado pelo interpretador do python. O comando print é o primeiro comando, é o básico e o essencial.

# Expressões
# Uma expressão é uma combinação de valores, variáveis e operadores. Um valor também é considerado uma expressão. Vamos ver alguns exemplos
42
'hello'


# Comentários
# São todas as partes que há uma hastag (#). Comentários não serão executados pelo programa, tudo que colocar nos comentários não será executado. São ótimos para explicação de código, desativação de algum comando e para pessoas entenderem o que siginifica tal instrução.