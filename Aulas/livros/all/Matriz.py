# listas aninhadas(matriz): Consiste na existência de uma lista como sendo elemento de uma outra lista: matriz [<índice-externo> [<índice-interno>]]
numeros = [[1, 2, 3], # co
           [4, 5, 6], # lu
           [7, 8, 9]] # na
           # linha
# Acessando um elemento: matriz[coluna][linha]
numeros[1][2] # resultado: 6