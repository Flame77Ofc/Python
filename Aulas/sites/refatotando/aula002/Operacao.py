# Declaração das variáveis
num1 = 5 # int
num2 = 7.4 # float

# Usando a função type para descobrir qual tipo de dado é uma determinada variável
print(type(num1))
print(type(num2))

# Trabalhando com operadores
print(5 + 6) # Adição
print(4 - 2) # Subtração
print(6 * 3) # Multiplicação
print(9 / 3) # Divisão
print(10 // 4) # Divisão de inteiro
print(16 % 3) # Resto da divisão
print(5**2) # Exponenciação

# Funções matemáticas
print(max(15, 24, -12, 67, 12, 45)) # Diz qual é o maior número
print(min(23, 56, -1, 32, 690)) # Diz qual é o menor número
print(round(8.5)) # Arredonda para o número mais próximo
import math
print(math.floor(9.9)) # Arredonda para baixo
print(math.ceil(9.1))# Arredonda para cima