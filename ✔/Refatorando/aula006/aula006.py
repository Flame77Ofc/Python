# Set e Dictionary

# Set: 
# Não tem uma ordem específica: São aleatórios
frutas = {'Maçã', 'Laranja', 'Abacaxi'}
# Não aceita valores repetidos:
frutas = {'Maçã', 'Laranja', 'Abacaxi', 'Laranja', 'Abacaxi'}
print(frutas)
print()
# Aceita tipos de dados diferentes. 
dadosFBI = {True, False, False, 'senha:123456', 837, 123, True, 'João', 'cofre:346577332', 56}
print(dadosFBI)
print()



