# Algumas funções
print(max(12, 34))
print(min(12, 34))
print(round(5.5)) # se for maior que 5: arredonda para cima, se não, para baixo.

# Importando o math
import math
print(math.floor(8.8)) # Arredondando para baixo
print(math.ceil(9.1)) # Arredondando para cima



# Outras funções com strings
string = 'ola mundo'
print(string.isupper()) # verifica se é maiúsculo e retorna um booleano
print(string.islower()) # verifica se é minúsculo e retorna um booleano
print(string.replace('ola', 'oi')) #substituir parte do texto