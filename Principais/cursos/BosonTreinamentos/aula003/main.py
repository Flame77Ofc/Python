# Funções matemáticas built-ins
valores = [3, 89, 345, 123, 0, 12, -45]
print(min(valores)) # Retorna o menor valor da lista
print(max(valores)) # Retorna o maior valor da lista

# round: Arredonda ou Ajusta o valor de casas decimais
a = 3.141598123
print(round(a)) # Arredonda para 3
print(round(a, 120)) # Mostra apenas as duas primeiras casas decimais

