# Lists - Arrays
numbers = [1,2,3,4,5,6]
print(numbers)



fruits = ['apple', 'watermelon', 'grapes', 'orange', 'strawberry', 'banana']
print(fruits)

# Add:
fruits.append('MORE GRAPESSSSSSS!!!!')
print(fruits)

# Index
print(fruits[2])
print(fruits[5])

# Slicing
print(fruits[2:6])
print(fruits[1:4])

# length
print(len(fruits))