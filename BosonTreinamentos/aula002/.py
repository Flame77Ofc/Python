# For e range
lista = [2,5,6,8,2,3,7,9]
lista.sort()
for item in lista:
    print(item)

nomes = ['Gabriel', 'Marco', 'Julia']
for nome in nomes:
    print(nome)

palavra = 'Flame'
for letra in palavra:
    print(letra)

for numero in range(1, 11):
    print(numero)

# len: Se for uma lista, mostra quantos itens há na lista, se for em string, mostra quantos caracteres tem.
comida = ['Arroz', 'Feijão']
print(len(comida))