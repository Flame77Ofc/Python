# Tuplas
# Tuplas são ordenadas (Mantém a ordem dos elementos) Imutáveis (Não é possível alterar o seu valor) e permitem valores duplicados
tupla = 'Gato', 'Elefante', 'Macaco', 'Rinoceronte'
print(tupla)


# Sets
# Set é uma coleção que não aceita valores duplicados e é desordenado
ids = {12, 54, 89}
print(ids)