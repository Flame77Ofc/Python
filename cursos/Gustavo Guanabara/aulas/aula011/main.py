# Listas
# Listas permitem a alteração de elementos
lanche = ['Suco', 'Pudim', 'Batata Frita', 'Pizza']
lanche[1] = 'Bolo'
print(lanche) # ['Suco', 'Bolo', 'Batata Frita', 'Pizza']

valores = list(range(1, 11))
print(valores)




numeros = [7, 12, 3, 5, 4, 9, 8]
numeros[4] = 5
numeros.append(4)
numeros.sort()

print(numeros)




